                    <h2>DRAINAGE</h2>
                </div>
                <div class="text" style="font-size: 16px;">
                    In het dagelijks leven heeft ons lichaam regelmatig te maken met
                    overbelasting door afvalstoffen. Enkele oorzaken van buitenaf
                    kunnen zijn: het inademen van vervuilde lucht (zware metalen) of
                    bepaalde stoffen zoals restanten van pesticiden, dioxines of
                    bepaalde toevoegingen in onze voeding. Daarnaast kan ons
                    lichaam zelf een overbelasting van afvalstoffen creëren doordat
                    bijvoorbeeld ons lymfesysteem of bloedcirculatie niet optimaal
                    functioneren. Ons lichaam is onder gunstige omstandigheden in
                    staat om de meeste afvalstoffen zonder problemen uit te
                    scheiden via onze uitscheidingsorganen. Soms echter is het
                    wenselijk om het lichaam daarin te ondersteunen en te
                    stimuleren. Wanneer er te veel afvalstoffen in het lichaam
                    aanwezig zijn, bijvoorbeeld in onze darmen, dan kunnen
                    voedingsstoffen ook niet goed meer worden opgenomen.
