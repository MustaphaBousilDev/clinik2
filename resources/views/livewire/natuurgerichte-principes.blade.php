                    <h2>PRIKKELOVERDRACHT</h2>
                </div>
                <div class="text" style="font-size: 16px;">
                    De prikkeloverdracht komt tot stand via je zenuwstelsel. Via de
hersenen worden prikkels via het zenuwstelsel naar de rest van je
lichaam gestuurd. Maar ook andersom worden prikkels vanuit je
lichaam naar je hersenen gestuurd. Het zenuwstelsel is het
communicatienetwerk ofwel ‘de telefoonlijn’ van je lichaam.
Pijnprikkels bijvoorbeeld hebben een functie om bij bepaalde
situaties juist en snel te kunnen reageren. De prikkeloverdracht
zorgt er ook voor dat bepaalde signalen, bijvoorbeeld over wat je
nodig hebt voor herstel, aan het lichaam worden doorgegeven.
Een goede prikkeloverdracht is onmisbaar om goed te kunnen
functioneren en herstellen.
